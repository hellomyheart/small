public class Switchs {
    public static void main(String[] args) {
        int s=4;
        switch (s){
            case 1:
                System.out.println("剪刀");
                    break;
            case 2:
                System.out.println("石头");
                break;
            case 3:
                System.out.println("布");
                break;
            default:
                System.out.println("无");
        }
    }



}
